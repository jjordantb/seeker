package io.paratek.seeker.scene.entity;

import java.io.Serializable;

public abstract class Entity implements Serializable {

    private int x, y;

    public int getX() {
        return x;
    }

    public void setX(int x) {
        this.x = x;
    }

    public int getY() {
        return y;
    }

    public void setY(int y) {
        this.y = y;
    }

}
